def create_database():
    """创建数据库和三张表结构"""
    conn = sqlite3.connect(DATABASE)
    cursor = conn.cursor()

    # 省份表
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS provinces (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT NOT NULL UNIQUE,
            capital TEXT,
            abbreviation TEXT,
            population REAL,
            area REAL,
            intro TEXT,
            culture TEXT
        )
    ''')

    # 城市表
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS cities (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT NOT NULL,
            province TEXT NOT NULL,
            longitude REAL NOT NULL,
            latitude REAL NOT NULL,
            level TEXT,
            intro TEXT,
            culture TEXT,
            population REAL,
            area REAL,
            gdp REAL,
            climate TEXT,
            attractions TEXT,
            established TEXT
        )
    ''')

    # 区县表
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS districts (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT NOT NULL,
            city_id INTEGER NOT NULL,
            city_name TEXT NOT NULL,
            province_name TEXT NOT NULL,
            longitude REAL NOT NULL,
            latitude REAL NOT NULL,
            type TEXT,
            population REAL,
            area REAL,
            FOREIGN KEY (city_id) REFERENCES cities(id)
        )
    ''')

    # 遥感研究数据表
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS research_points (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            longitude REAL NOT NULL,
            latitude REAL NOT NULL,
            author TEXT NOT NULL,
            institution TEXT NOT NULL,
            overview TEXT NOT NULL,
            title TEXT,
            province TEXT NOT NULL,
            year INTEGER
        )
    ''')

    # 索引
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_city_province ON cities(province)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_district_city ON districts(city_id)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_district_province ON districts(province_name)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_district_coords ON districts(latitude, longitude)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_research_province ON research_points(province)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_research_year ON research_points(year)')

    conn.commit()
    conn.close()
    print('[OK] 数据库与表结构创建完成（provinces / cities / districts / research_points）')


