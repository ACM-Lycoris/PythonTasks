def insert_districts():
    """插入区县数据（精选 + 程序化生成）"""
    from districts_data import get_all_districts

    conn = sqlite3.connect(DATABASE)
    cursor = conn.cursor()
    cursor.execute('DELETE FROM districts')

    # 从数据库读取所有城市信息
    cursor.execute('SELECT id, name, province, longitude, latitude, level, population FROM cities')
    cities = []
    for row in cursor.fetchall():
        cities.append({
            'id': row[0],
            'name': row[1],
            'province': row[2],
            'longitude': row[3],
            'latitude': row[4],
            'level': row[5],
            'population': row[6]
        })

    # 生成区县数据
    districts = get_all_districts(cities)

    # 批量插入
    cursor.executemany('''
        INSERT INTO districts (name, city_id, city_name, province_name,
                               longitude, latitude, type, population, area)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
    ''', districts)

    conn.commit()
    cursor.execute('SELECT COUNT(*) FROM districts')
    cnt = cursor.fetchone()[0]
    conn.close()
    print(f'[OK] 已插入 {cnt} 条区县级行政区数据')


