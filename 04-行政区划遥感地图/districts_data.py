# 程序化生成函数: 对于不在CURATED_DISTRICTS中的城市
# ============================================================
def generate_districts_for_city(city_name, province_name, city_lat, city_lng, city_population, city_level):
    """
    根据城市参数程序化生成区县数据。
    生成的区县名称遵循中国实际命名模式。
    """
    if city_name in CURATED_DISTRICTS:
        return None  # 使用精选数据

    districts = []
    pop = city_population or 300

    # 根据人口和级别确定区县数量
    if city_level in ('直辖市', '副省级', '省会'):
        num_urban = random.randint(5, 10)
        num_county = random.randint(3, 7)
    elif city_level == '地级市':
        num_urban = max(2, min(8, int(pop / 100)))
        num_county = max(2, min(10, int(pop / 80)))
    elif city_level in ('自治州', '地区', '盟'):
        num_urban = 1
        num_county = max(3, min(12, int(pop / 60)))
    elif city_level == '省直辖县级市':
        num_urban = 1
        num_county = 1
    else:
        num_urban = random.randint(2, 5)
        num_county = random.randint(2, 6)

    total = num_urban + num_county

    # 生成区名 -- 使用常见的命名模式
    district_prefixes = ['东', '西', '南', '北', '中', '新', '兴', '华', '城', '滨',
                         '江', '河', '湖', '海', '山', '林', '安', '宁', '平', '和',
                         '昌', '盛', '丰', '泰', '永', '长', '文', '武', '龙', '凤']
    county_prefixes = ['安', '宁', '平', '和', '昌', '盛', '丰', '泰', '永', '兴',
                       '新', '华', '文', '武', '龙', '凤', '长', '乐', '康', '定']

    urban_names_used = set()
    county_names_used = set()

    for i in range(num_urban):
        # 生成不重复的区名
        for _ in range(100):
            prefix = random.choice(district_prefixes)
            suffix = random.choice(['城', '安', '宁', '平', '和', '昌', '兴', '华', '阳', '州',
                                    '东', '西', '南', '北', '湖', '山', '林', '港', '江', '海'])
            if prefix != suffix:
                name = prefix + suffix + '区'
                if name not in urban_names_used:
                    urban_names_used.add(name)
                    break
        else:
            name = f'{city_name}第{i+1}区'

        angle = (i / max(num_urban, 1)) * 2 * math.pi
        distance = random.uniform(2, 15)
        dlat = distance / 111.0 * math.cos(angle)
        dlng = distance / (111.0 * math.cos(max(abs(city_lat), 0.01) * math.pi / 180)) * math.sin(angle)

        districts.append({
            'name': name, 'type': '市辖区',
            'lat': round(city_lat + dlat, 4), 'lng': round(city_lng + dlng, 4),
            'population': round(random.uniform(20, pop * 0.4 / num_urban), 1),
            'area': round(random.uniform(30, 600), 0)
        })

    # 生成县
    for i in range(num_county):
        for _ in range(100):
            prefix = random.choice(county_prefixes)
            suffix = random.choice(['安', '宁', '平', '和', '昌', '兴', '华', '阳', '州', '化',
                                    '远', '山', '川', '河', '城', '陵', '丰', '定', '康', '乐'])
            if prefix != suffix:
                # 约15%几率生成自治县
                if random.random() < 0.15 and province_name in (
                    '广西壮族自治区', '云南省', '贵州省', '湖南省', '湖北省',
                    '四川省', '青海省', '甘肃省', '吉林省', '辽宁省',
                    '内蒙古自治区', '新疆维吾尔自治区'
                ):
                    ethnic = random.choice(['苗族', '土家族', '彝族', '壮族', '回族',
                                            '藏族', '蒙古族', '满族', '瑶族', '侗族'])
                    name = prefix + suffix + ethnic + '自治县'
                else:
                    name = prefix + suffix + '县'
                if name not in county_names_used:
                    county_names_used.add(name)
                    break
        else:
            name = f'{city_name}第{i+1}县'

        angle = (i / max(num_county, 1)) * 2 * math.pi + math.pi / num_county
        distance = random.uniform(25, 80)
        dlat = distance / 111.0 * math.cos(angle)
        dlng = distance / (111.0 * math.cos(max(abs(city_lat), 0.01) * math.pi / 180)) * math.sin(angle)

        districts.append({
            'name': name, 'type': '县',
            'lat': round(city_lat + dlat, 4), 'lng': round(city_lng + dlng, 4),
            'population': round(random.uniform(10, pop * 0.25 / max(num_county, 1)), 1),
            'area': round(random.uniform(200, 3000), 0)
        })

    # 较大的城市可能有1-2个县级市
    if pop > 300 and num_urban > 3 and random.random() < 0.6:
        for i in range(random.randint(1, 2)):
            prefix = random.choice(district_prefixes)
            suffix = random.choice(['城', '州', '阳', '安', '平', '兴', '宁', '化', '远', '陵'])
            if prefix != suffix:
                name = prefix + suffix + '市'
                angle = random.uniform(0, 2 * math.pi)
                distance = random.uniform(30, 70)
                dlat = distance / 111.0 * math.cos(angle)
                dlng = distance / (111.0 * math.cos(max(abs(city_lat), 0.01) * math.pi / 180)) * math.sin(angle)
                districts.append({
                    'name': name, 'type': '县级市',
                    'lat': round(city_lat + dlat, 4), 'lng': round(city_lng + dlng, 4),
                    'population': round(random.uniform(30, 150), 1),
                    'area': round(random.uniform(500, 2500), 0)
                })

    return districts if districts else None


