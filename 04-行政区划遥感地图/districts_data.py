# 主函数: 获取所有区县数据
# ============================================================
def get_all_districts(cities):
    """
    cities: list of dicts from the database, with keys:
        id, name, province, longitude, latitude, level, population

    Returns: list of tuples ready for INSERT:
        (name, city_id, city_name, province_name, longitude, latitude, type, population, area)
    """
    all_districts = []
    curated_count = 0
    generated_count = 0

    for city in cities:
        city_name = city['name']
        province_name = city['province']
        city_lat = city['latitude']
        city_lng = city['longitude']
        city_pop = city['population'] or 300
        city_level = city['level'] or '地级市'
        city_id = city['id']

        # 先查精选数据
        if city_name in CURATED_DISTRICTS:
            curated_list = CURATED_DISTRICTS[city_name]
            for i, (dist_name, dist_type) in enumerate(curated_list):
                # 围绕城市中心分布
                angle = (i / max(len(curated_list), 1)) * 2 * math.pi
                if dist_type == '市辖区':
                    distance = random.uniform(2, 15)
                elif dist_type == '县级市':
                    distance = random.uniform(25, 60)
                else:
                    distance = random.uniform(20, 80)

                dlat = distance / 111.0 * math.cos(angle)
                dlng = distance / (111.0 * math.cos(max(abs(city_lat), 0.01) * math.pi / 180)) * math.sin(angle)

                all_districts.append((
                    dist_name,
                    city_id,
                    city_name,
                    province_name,
                    round(city_lng + dlng, 4),
                    round(city_lat + dlat, 4),
                    dist_type,
                    round(random.uniform(15, 200), 1),  # population
                    round(random.uniform(30, 2500), 0)   # area
                ))
                curated_count += 1
        else:
            # 程序化生成
            generated = generate_districts_for_city(
                city_name, province_name, city_lat, city_lng, city_pop, city_level
            )
            if generated:
                for d in generated:
                    all_districts.append((
                        d['name'],
                        city_id,
                        city_name,
                        province_name,
                        d['lng'],
                        d['lat'],
                        d['type'],
                        d['population'],
                        d['area']
                    ))
                    generated_count += 1

    print(f'[Districts] 精选: {curated_count}, 生成: {generated_count}, 总计: {len(all_districts)}')
    return all_districts
